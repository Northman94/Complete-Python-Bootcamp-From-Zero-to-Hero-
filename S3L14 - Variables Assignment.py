# Naming variables Python Rules:

# Not starting with a number;

# No spaces in the name. Only "_" as a space.

# No symbols:     : ’ " , < > / ? | \ ( ) ! @ # $ % ^ & * ~ - +



# Best (PEP8) practice - names are Lowercase. And later on Global variables in Uppercase.


# Avoid using reserved words such as e.g. "list", "str".


